/// Enum for the different types of component status.
#[allow(dead_code)]
pub enum ComponentStatus{
	Info,
	Success,
	Neutral,
	Warning,
	Error
}

/// Enum for the different types of colors.
#[allow(dead_code)]
pub enum Colors{
	Black,
	White,
	Gray
}

/// Enum for the different positions of the modal.
#[allow(dead_code)]
pub enum Position{
	TopLeft,
	TopCenter,
	TopRight,
	Center,
	BottomLeft,
	BottomCenter,
	BottomRight,
}